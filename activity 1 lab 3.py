x=25
y=10
# Addition operator (+):
print(x+y)
#Subtraction operator (-):
print(x-y)
#Multiplication operator (*):
print(x*y)
#Division operator (/):
print(x/y)
#Modulus (Or, Remainder) operator (%)
print(x%y)
#Exponentiation operator (**)
print(y**3)
#Floor Division (//) operator:
print(x//y)